        if (cstmt != null) {
            try {
                cstmt.close();
            } catch (SQLException ex) {
                System.out.println(ex.toString());
            }

        }

        JFreeChart grafico2;
        DefaultCategoryDataset datos2 = new DefaultCategoryDataset();
        Integer min = 0;
        Integer upper100 = 0;
        Integer upper200 = 0;
        Integer upper300 = 0;
        Integer upper400 = 0;

        for (int i = 0; i < this.incomeTable.getRowCount(); i++) {

            Double income = Double.parseDouble((String) incomeTable.getValueAt(i, 2));

            if (income <= 10000d) {
                min++;
            }
            if (income > 100000d) {
                upper100++;
            }
            if (income > 200000d) {
                upper200++;
            }
            if (income > 300000d) {
                upper300++;
            }
            if (income > 400000d) {
                upper400++;
            }

        }

        datos2.addValue(min, "Low 50 000", "Comparable");
        datos2.addValue(upper100, "Up 100 000", "Comparable");
        datos2.addValue(upper200, "Up 200 000", "Comparable");
